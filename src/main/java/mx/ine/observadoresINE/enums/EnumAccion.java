public enum EnumAccion {

	C("Captura"),
	CO("Consulta"),
	M("Modifica");
	
	private final String description;
	
	/**
	 * Constructor para poder agregar el valor de la descripción de cada enum
     * 
     * @param description
	 * @author Emmanuel García Ysamit
	 * @since 19/07/2017
	 */
	EnumAccion(String description){
		this.description = description;
	}
	
	/**
	 * Método que obtiene el valor de el atributo description
     *
     * @return String : valor que tiene el atributo description
	 * @author Emmanuel García Ysamit
	 * @since 19/07/2017
	 */
	public String getDescription(){
		return description;
	}
}
